    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed

        DecimalFormat df = new DecimalFormat("#.##");

        int x;
        posProduto = -1;

        for (x = 0; x < produto.size(); x++) {
            if (produto.get(x).getCodigo().equals(txtCodProduto.getText())) {
                posProduto = x;
            }
        }

        if (Double.parseDouble(txtQtdeVendida.getText()) == 0) {
            JOptionPane.showMessageDialog(null, "Quantidade inválida. Insira um valor maior que zero!", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {

            if (Double.parseDouble(txtQtdeVendida.getText()) > produto.get(posProduto).getQtdeEstoque()) {
                JOptionPane.showMessageDialog(null, "Quantidade em estoque menor do que o solicitado!", "Aviso", JOptionPane.ERROR_MESSAGE);
            } else {

                if (objPedido.getCliente().getLimiteDisp() < (Double.parseDouble(txtQtdeVendida.getText()) * produto.get(posProduto).getPreco())) {
                    JOptionPane.showMessageDialog(null, "Limite indisponível!", "Aviso", JOptionPane.ERROR_MESSAGE);
                } else {
                    
                    objItemPedido = new ItemPedido(modTblPedido.getRowCount() + 1, Double.parseDouble(txtQtdeVendida.getText()), produto.get(posProduto));

                    String linha[] = {txtCodProduto.getText(), lblDescricao.getText(), String.valueOf(objItemPedido.getProduto().getPreco()), txtQtdeVendida.getText()};

                    subtotal = Double.parseDouble(txtQtdeVendida.getText()) * objItemPedido.getProduto().getPreco();
                    
                    modTblPedido.addRow(linha);
                    modTblPedido.setValueAt(df.format(subtotal), modTblPedido.getRowCount() - 1, 4);

                    total += subtotal;
                    
                    objPedido.addItemPedido(objItemPedido);
                    
                    lblValTotal.setText(String.valueOf(total));
                    lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));
                    
                    txtCodProduto.setText(null);
                    lblDescricao.setText(" ");
                    txtQtdeVendida.setText("");
                    
                    txtCodProduto.requestFocus();
                    
                }

            }
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed

        if(tblItemPedido.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(null, "A linha não foi selecionada!", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
            String codigo;
            double qtdeVend;

            codigo = (String) modTblPedido.getValueAt(tblItemPedido.getSelectedRow(), 0);

            qtdeVend = Double.parseDouble((String) modTblPedido.getValueAt(tblItemPedido.getSelectedRow(), 3));

            if(objPedido.getItemPedidos().get(tblItemPedido.getSelectedRow()).getProduto().getCodigo().equals(codigo)){

                objPedido.getItemPedidos().get(tblItemPedido.getSelectedRow()).getProduto().setQtdeEstoque(
                        objPedido.getItemPedidos().get(tblItemPedido.getSelectedRow()).getProduto().getQtdeEstoque() + qtdeVend);

                objPedido.getCliente().setLimiteDisp(objPedido.getCliente().getLimiteDisp()
                    + Double.parseDouble((String) modTblPedido.getValueAt(tblItemPedido.getSelectedRow(), 4)));

            }

            objPedido.getItemPedidos().remove(tblItemPedido.getSelectedRow());

            total -= Double.parseDouble((String) modTblPedido.getValueAt(tblItemPedido.getSelectedRow(), 4));

            modTblPedido.removeRow(tblItemPedido.getSelectedRow());

            lblValTotal.setText(String.valueOf(total));
            lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));
            
            txtCodProduto.requestFocus();

        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

