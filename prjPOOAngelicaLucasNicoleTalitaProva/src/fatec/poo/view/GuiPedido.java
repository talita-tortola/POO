    private void btnConsultarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPedidoActionPerformed

        DecimalFormat df = new DecimalFormat("#.##");

        int x;
        posPedido = -1;

        for (x = 0; x < pedido.size(); x++) {
            if (pedido.get(x).getNumero().equals(txtNumPedido.getText())) {
                posPedido = x;
            }
        }

        if (posPedido > -1) {
            objPedido = pedido.get(posPedido);

            txtDtPedido.setText(objPedido.getDataEmissao());
            
            if(objPedido.getFormaPagto() == true){
                cbxFormaPagto.setSelectedIndex(0);
            }else{
                cbxFormaPagto.setSelectedIndex(1);
            }
            
            txtCPFCliente.setText(objPedido.getCliente().getCpf());
            lblNomeCliente.setText(objPedido.getCliente().getNome());
            txtCPFVendedor.setText(objPedido.getVendedor().getCpf());
            lblNomeVendedor.setText(objPedido.getVendedor().getNome());

            for (int i = 0; i < objPedido.getItemPedidos().size(); i++){
                
                String linha[] = {objPedido.getItemPedidos().get(i).getProduto().getCodigo(),
                    objPedido.getItemPedidos().get(i).getProduto().getDescricao(),
                    String.valueOf(objPedido.getItemPedidos().get(i).getProduto().getPreco()),
                    String.valueOf(objPedido.getItemPedidos().get(i).getQtdeVendida())};

                subtotal = objPedido.getItemPedidos().get(i).getQtdeVendida()
                        * objPedido.getItemPedidos().get(i).getProduto().getPreco();

                modTblPedido.addRow(linha);
                modTblPedido.setValueAt(df.format(subtotal), modTblPedido.getRowCount() - 1, 4);

                total += subtotal;

                lblValTotal.setText(String.valueOf(total));
                lblQtdeItensPedidos.setText(String.valueOf(modTblPedido.getRowCount()));

            }

            txtNumPedido.setEnabled(false);
            txtDtPedido.setEnabled(false);
            cbxFormaPagto.setEnabled(true);
            txtCPFCliente.setEnabled(false);
            txtCPFVendedor.setEnabled(false);
            txtCodProduto.setEnabled(true);
            txtQtdeVendida.setEnabled(true);

            btnConsultarPedido.setEnabled(false);
            btnConsultarCliente.setEnabled(false);
            btnConsultarVendedor.setEnabled(false);
            btnConsultarProduto.setEnabled(true);
            btnAdicionar.setEnabled(true);
            btnRemover.setEnabled(true);
            btnAlterar.setEnabled(true);
            btnExcluir.setEnabled(true);
            
            cbxFormaPagto.requestFocus();

        } else {

            txtDtPedido.setEnabled(true);
            cbxFormaPagto.setEnabled(true);
            txtCPFCliente.setEnabled(true);

            btnConsultarPedido.setEnabled(false);
            btnConsultarCliente.setEnabled(true);

            txtDtPedido.requestFocus();
            
        }

    }//GEN-LAST:event_btnConsultarPedidoActionPerformed

    private void btnConsultarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarClienteActionPerformed

        objPedido = new Pedido(txtNumPedido.getText(), txtDtPedido.getText().replaceAll("/", ""));
        
        if(cbxFormaPagto.getSelectedIndex() == 0){
            objPedido.setFormaPagto(true);
        }else{
            objPedido.setFormaPagto(false);
        }

        int x;
        posCliente = -1;

        for (x = 0; x < cliVend.size(); x++) {
            if ((cliVend.get(x) instanceof Cliente) && (cliVend.get(x).getCpf().equals(txtCPFCliente.getText().replaceAll("[\\.\\-]", "")))) {
                posCliente = x;
            }
        }

        if (posCliente > -1) {
            objCli = (Cliente) cliVend.get(posCliente);

            lblNomeCliente.setText(objCli.getNome());

            txtDtPedido.setEnabled(false);
            cbxFormaPagto.setEnabled(false);
            txtCPFCliente.setEnabled(false);
            btnConsultarCliente.setEnabled(false);
            txtCPFVendedor.setEnabled(true);
            btnConsultarVendedor.setEnabled(true);

            objCli.addPedido(objPedido);
            
            txtCPFVendedor.requestFocus();

        } else {
            JOptionPane.showMessageDialog(null, "CPF inserido não existe!", "Aviso", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnConsultarClienteActionPerformed

